def categorize_polygon_change(pct):
    abs_pct = abs(pct)
    if abs_pct < 2:
        return '0-2%'
    elif abs_pct < 5:
        return '2-5%'
    elif abs_pct < 10:
        return '5-10%'
    elif abs_pct < 20:
        return '10-20%'
    else:
        return '20%+'


def categorize_jsi(jsi: float) -> str:
    """Categorize Jaccard Similarity Index into agreement levels."""
    if jsi >= 0.95:
        return 'Excellent (≥0.95)'
    elif jsi >= 0.90:
        return 'Good (0.90-0.95)'
    elif jsi >= 0.80:
        return 'Moderate (0.80-0.90)'
    elif jsi >= 0.70:
        return 'Poor (0.70-0.80)'
    else:
        return 'Very Poor (<0.70)'


