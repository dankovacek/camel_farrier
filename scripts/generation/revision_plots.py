def categorize_backlog_duration(days):
    """Categorize time since last data record.

    Args:
        days: Number of days since last data record

    Returns:
        Category string describing backlog duration
    """
    if days < 365:  # < 1 year
        return '<1 year'
    elif days < 730:  # 1-2 years
        return '1-2 years'
    elif days < 1825:  # 2-5 years
        return '2-5 years'
    elif days < 3650:  # 5-10 years
        return '5-10 years'
    else:
        return '10+ years'


