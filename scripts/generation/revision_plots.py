def plot_revision_map(data: pd.DataFrame = None):
    """Generate interactive map showing station locations colored by area change.

    Args:
        data: Optional DataFrame with comparison metrics. If None, loads
              data from centralized CSV.

    Returns:
        Bokeh figure with tile provider background and colored markers
    """
    if data is None:
        data = load_revision_metrics()

    df = data

    if df.empty:
        from bokeh.models import Div
        return Div(text="<p>No comparison data available.</p>")

    # Remove stations without coordinates
    df = df.dropna(subset=['latitude', 'longitude'])

    if df.empty:
        from bokeh.models import Div
        return Div(text="<p>No stations with valid coordinates.</p>")

    gdf = gpd.GeoDataFrame(df, geometry=gpd.points_from_xy(df.longitude, df.latitude), crs="EPSG:4326")
    gdf = gdf.to_crs(epsg=3857)  # Web Mercator for plotting

    # Convert lat/lon to Web Mercator for tile overlay
    df['x'] = gdf.geometry.x
    df['y'] = gdf.geometry.y

    # Categorize percent area differences into 5 bins

    df['category'] = df['percent_area_diff'].apply(categorize_polygon_change)
    df['color'] = df['category'].map(category_colors)

    # Create figure with tile provider
    p = figure(
        title="",
        x_axis_type="mercator",
        y_axis_type="mercator",
        width=800,
        height=600,
        tools="pan,wheel_zoom,lasso_select,box_select,reset,save",
        toolbar_location="above",
        match_aspect=True, # keep aspect ratio
    )
    p.add_tile("CartoDB Positron", retina=True)

    df['url'] = df['station_id'].apply(_station_page_url)
    df['map_url'] = df['url'].apply(lambda v: v if v else '#')

    # Create per-category renderers for interactive legend
    legend_items = []
    renderers = []

    for cat in ['0-2%', '2-5%', '5-10%', '10-20%', '20%+']:
        cat_df = df[df['category'] == cat]
        if not cat_df.empty:
            cat_source = ColumnDataSource(cat_df)
            renderer = p.scatter(
                'x', 'y',
                source=cat_source,
                size=12,
                color=category_colors[cat],
                line_color='black',
                line_width=1,
                alpha=0.8,
                legend_label=cat
            )
            renderers.append(renderer)
            legend_items.append(LegendItem(label=cat, renderers=[renderer]))

    # Add hover tool (works on all renderers)
    hover = HoverTool(
        tooltips=[
            ("Station", "@station_id"),
            ("Jaccard Index", "@jaccard_index{0.00}"),
            ("Percent Area Change", "@percent_area_diff{0.0}%"),
            ("Category", "@category"),
            ("", "Click to view station page")
        ]
    )
    p.add_tools(hover)

    # Add tap tool for clickable links to station pages
    tap = TapTool()
    p.add_tools(tap)
    p.select(type=TapTool).callback = OpenURL(url='@map_url')

    # Configure legend
    p.legend.location = "top_right"
    p.legend.title = "Area Change"
    p.legend.title_text_font_style = "bold"
    p.legend.click_policy = "hide"

    return p


def plot_jaccard_cdf(data: pd.DataFrame = None):
    """Generate CDF plot for Jaccard similarity indices.

    Args:
        data: Optional DataFrame with comparison metrics. If None, loads
              data from centralized CSV.

    Returns:
        Bokeh figure with CDF line and percentile annotations
    """
    if data is None:
        data = load_revision_metrics()

    df = data
    df = df[df['jaccard_index'].notnull()]

    if df.empty:
        from bokeh.models import Div
        return Div(text="<p>No comparison data available.</p>")

    # Calculate CDF
    sorted_values = np.sort(df['jaccard_index'].values)
    yvals = np.arange(1, len(sorted_values) + 1) / len(sorted_values)

    # Create figure
    p = figure(
        title="Cumulative Distribution of Jaccard Similarity Indices",
        x_axis_label="Jaccard Similarity Index",
        y_axis_label="Cumulative Probability",
        width=700,
        height=400,
        tools="pan,wheel_zoom,lasso_select,box_select,reset,save",
        toolbar_location="right",
    )
    assert len(sorted_values) == len(yvals), f'Length mismatch: {len(sorted_values)} vs {len(yvals)}'

    # Plot CDF
    p.line(sorted_values, yvals, line_width=2, color='navy', legend_label='JSI CDF')
    p.scatter(sorted_values, yvals, size=4, color='navy', alpha=0.6)

    # Add percentile annotations
    percentiles = [25, 50, 75]
    for pct in percentiles:
        value = np.percentile(sorted_values, pct)
        p.line([value, value], [0.01, pct/100], line_dash='dashed',
               line_width=1, color='gray', alpha=0.5)
        p.line([sorted_values.min(), value], [pct/100, pct/100],
               line_dash='dashed', line_width=1, color='gray', alpha=0.5)

    # Styling
    p.legend.location = "top_left"
    p.y_range.start = 0
    p.y_range.end = 1

    return p


def plot_area_change_cdf(data: pd.DataFrame = None):
    """Generate CDF plot for percent area differences.

    Args:
        data: Optional DataFrame with comparison metrics. If None, loads
              data from centralized CSV.

    Returns:
        Bokeh figure with CDF line and reference lines
    """
    if data is None:
        data = load_revision_metrics()

    df = data

    if df.empty:
        from bokeh.models import Div
        return Div(text="<p>No comparison data available. Run polygon integration first.</p>")

    # Calculate CDF using absolute percent change
    sorted_values = np.sort(np.abs(df['percent_area_diff'].values)) + 0.001
    yvals = np.arange(1, len(sorted_values) + 1) / len(sorted_values)

    # Create figure
    p = figure(
        title="Cumulative Distribution of Absolute Percent Area Changes",
        x_axis_label="Absolute Percent Area Change (%)",
        y_axis_label="Cumulative Probability",
        width=700,
        height=400,
        tools="pan,wheel_zoom,lasso_select,box_select,reset,save",
        x_axis_type='log'
    )

    # Plot CDF
    p.line(sorted_values, yvals, line_width=2, color='darkred',
           legend_label='|ΔArea| CDF')
    p.scatter(sorted_values, yvals, size=4, color='darkred', alpha=0.6)

    # Add percentile annotations
    percentiles = [50, 75, 90]
    for pct in percentiles:
        value = np.percentile(sorted_values, pct)
        p.line([value, value], [0, pct/100], line_dash='dashed',
               line_width=1, color='gray', alpha=0.5)

    # Styling
    p.legend.location = "bottom_right"
    p.x_range.start = 0.9e-4
    p.y_range.start = 0
    p.y_range.end = 1

    return p


