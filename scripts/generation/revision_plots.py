def _fetch_station_coordinates(station_ids: List[str]) -> pd.DataFrame:
    """Fetch latitude/longitude for the provided station IDs from HYDAT."""
    if not station_ids or not HYDAT_DB_PATH.exists():
        return pd.DataFrame()

    placeholders = ",".join(["?"] * len(station_ids))
    query = (
        "SELECT STATION_NUMBER, LATITUDE, LONGITUDE FROM STATIONS "
        f"WHERE STATION_NUMBER IN ({placeholders})"
    )

    conn = sqlite3.connect(HYDAT_DB_PATH)
    try:
        coords = pd.read_sql_query(query, conn, params=station_ids)
    finally:
        conn.close()

    return coords.rename(columns={
        'STATION_NUMBER': 'station_id',
        'LATITUDE': 'latitude',
        'LONGITUDE': 'longitude'
    })


def _station_page_url(station_id: str) -> str:
    """Return relative station page link.

    Checks the built HTML in _build/html first; falls back to checking the
    source .md file.  Returns empty string only if neither exists.
    """
    built_html = BASE_DIR / "book_docs" / "_build" / "html" / "station_pages" / "stations" / f"{station_id}.html"
    if built_html.exists():
        return f"../station_pages/stations/{station_id}.html"
    source_md = STATION_PAGES_DIR / f"{station_id}.md"
    if source_md.exists():
        return f"../station_pages/stations/{station_id}.html"
    return ""


def _prepare_caravan_plot_dataframe() -> pd.DataFrame:
    """Prepare Caravan comparison data with geometry-derived plot columns."""
    df = load_caravan_comparison_data(include_coordinates=True)
    if df.empty:
        return df

    df = df.dropna(subset=['latitude', 'longitude']).copy()
    if df.empty:
        return df

    gdf = gpd.GeoDataFrame(df,
                           geometry=gpd.points_from_xy(df.longitude, df.latitude),
                           crs="EPSG:4326")
    gdf = gdf.to_crs(epsg=3857)

    df['x'] = gdf.geometry.x
    df['y'] = gdf.geometry.y
    df['category'] = df['percent_area_diff'].apply(categorize_polygon_change)
    df['color'] = df['category'].map(category_colors)
    df['url'] = df['station_id'].apply(_station_page_url)
    df['map_url'] = df['url'].apply(lambda val: val if val else '#')
    df['abs_percent_area_diff'] = df['percent_area_diff'].abs()
    df['abs_percent_area_diff_log'] = df['abs_percent_area_diff'].clip(lower=1e-3)

    return df


def _build_caravan_map(df: pd.DataFrame, source: ColumnDataSource):
    """Create Caravan comparison map sharing a ColumnDataSource."""
    p = figure(
        title="",
        x_axis_type="mercator",
        y_axis_type="mercator",
        width=800,
        height=600,
        tools="pan,wheel_zoom,lasso_select,box_select,reset,save",
        toolbar_location="above",
        match_aspect=True, # keep aspect ratio
    )
    p.add_tile("CartoDB Positron", retina=True)

    renderers = []
    for cat in ['0-2%', '2-5%', '5-10%', '10-20%', '20%+']:
        if cat not in df['category'].values:
            continue
        view = CDSView(filter=GroupFilter(column_name='category', group=cat))
        circles = p.scatter(
            'x', 'y',
            source=source,
            view=view,
            size=12,
            color=category_colors[cat],
            line_color='black',
            line_width=1,
            alpha=0.8,
            selection_color=category_colors[cat],
            nonselection_alpha=0.2,
            legend_label=cat
        )
        renderers.append(circles)

    hover = HoverTool(
        renderers=renderers,
        tooltips=[
            ("Station", "@station_id"),
            ("Jaccard Index", "@jaccard_index{0.00}"),
            ("Percent Area Change", "@percent_area_diff{0.0}%"),
            ("Category", "@category"),
            ("", "Click to view station page")
        ]
    )
    p.add_tools(hover)

    tap = TapTool()
    p.add_tools(tap)
    p.select(type=TapTool).callback = OpenURL(url='@map_url')

    p.legend.location = "top_right"
    p.legend.title = "Area Change"
    p.legend.title_text_font_style = "bold"
    p.legend.click_policy = "hide"

    return p


def _build_caravan_cdf(source: ColumnDataSource, metric: str):
    """Create CDF plot linked to the shared ColumnDataSource."""
    if metric == 'area':
        title = "Cumulative Distribution of Absolute Percent Area Changes"
        x_axis_label = "Absolute Percent Area Change (%)"
        x_axis_type = 'log'
        tooltip_label = "|ΔArea|"
        tooltip_field = '@abs_percent_area_diff{0.0}%'
    else:
        title = "Cumulative Distribution of Jaccard Similarity Indices"
        x_axis_label = "Jaccard Similarity Index"
        x_axis_type = 'linear'
        tooltip_label = "Jaccard Index"
        tooltip_field = '@jaccard_index{0.000}'

    p = figure(
        title=title,
        x_axis_label=x_axis_label,
        y_axis_label="Cumulative Probability",
        width=800,
        height=300,
        tools="pan,box_select,lasso_select,wheel_zoom,reset,save",
        toolbar_location="right",
        x_axis_type=x_axis_type
    )

    cdf_line = p.line('cdf_x', 'cdf_y', source=source, line_width=2, color='gray', alpha=0.6)
    cdf_points = p.scatter('cdf_x', 'cdf_y', source=source, size=6, color='color', alpha=0.8)

    hover = HoverTool(renderers=[cdf_points], tooltips=[
        ("Station", "@station_id"),
        (tooltip_label, tooltip_field)
    ])
    p.add_tools(hover)

    p.y_range.start = -0.01
    p.y_range.end = 1.01

    return p


def plot_caravan_map_with_cdf(metric: str = 'jaccard'):
    """Generate linked Caravan comparison map and CDF plots."""
    metric = metric.lower()
    df = _prepare_caravan_plot_dataframe()
    if df.empty:
        from bokeh.models import Div
        return Div(text="<p>No comparison data available.</p>")

    if metric == 'area':
        sort_col = 'abs_percent_area_diff'
        df_sorted = df.sort_values(sort_col).reset_index(drop=True)
        df_sorted['cdf_x'] = df_sorted['abs_percent_area_diff_log']
    else:
        sort_col = 'jaccard_index'
        df_sorted = df.sort_values(sort_col).reset_index(drop=True)
        df_sorted['cdf_x'] = df_sorted['jaccard_index']
        metric = 'jaccard'

    df_sorted['cdf_y'] = (np.arange(len(df_sorted)) + 1) / len(df_sorted)

    source = ColumnDataSource(df_sorted)

    map_plot = _build_caravan_map(df_sorted, source)
    cdf_plot = _build_caravan_cdf(source, metric)

    return column(map_plot, cdf_plot)


def load_caravan_comparison_data(include_coordinates: bool = True) -> pd.DataFrame:
    """Load Caravan vs. WSC comparison data and optionally add coordinates."""
    csv_path = BASE_DIR / "book_docs/data/polygon_comparisons/caravan_vs_wsc2024/comparison_metrics.csv"

    if not csv_path.exists():
        # Return empty DataFrame with expected column structure
        return pd.DataFrame(columns=[
            'station_id', 'jaccard_index', 'percent_area_diff',
            'absolute_area_diff_km2', 'old_area_km2', 'new_area_km2'
        ])

    df = pd.read_csv(csv_path)

    if df.empty or not include_coordinates:
        return df

    has_latlon = {'latitude', 'longitude'}.issubset(df.columns)
    if has_latlon and df[['latitude', 'longitude']].notna().any().all():
        return df

    station_ids = sorted(df['station_id'].astype(str).unique())
    coords = _fetch_station_coordinates(station_ids)
    if coords.empty:
        return df

    merged = df.merge(coords, on='station_id', how='left')
    return merged


def summarize_caravan_comparison(df: Optional[pd.DataFrame] = None):
    """Compute headline statistics for the Caravan comparison dataset and return DataFrame.

    Args:
        df: Optional DataFrame with comparison data

    Returns:
        DataFrame with Metric and Value columns
    """
    if df is None:
        df = load_caravan_comparison_data(include_coordinates=False)

    if df is None or df.empty:
        return pd.DataFrame()

    # Create summary as a vertical table (Metric, Value)
    rows = [
        ('Stations compared', str(int(len(df)))),
        ('Area change > 10%', f"{(df['percent_area_diff'].abs() > 10).mean() * 100:.1f}%"),
        ('JSI < 0.80', f"{(df['jaccard_index'] < 0.8).mean() * 100:.1f}%"),
        ('Median Jaccard index', f"{df['jaccard_index'].median():.3f}"),
        ('Median |Δarea| (%)', f"{df['percent_area_diff'].abs().median():.2f}%"),
        ('Median |Δarea| (km²)', f"{df['absolute_area_diff_km2'].median():.2f}"),
        ('Largest |Δarea| (km²)', f"{df['absolute_area_diff_km2'].max():.1f}")
    ]

    summary_df = pd.DataFrame(rows, columns=['Metric', 'Value'])
    return summary_df


