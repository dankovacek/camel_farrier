def collect_backlog_data():
    """Collect reporting backlog data for all active HYDAT stations.

    Returns:
        DataFrame with backlog data or None if error/no data
    """
    if not HYDAT_DB_PATH.exists():
        return None

    conn = sqlite3.connect(HYDAT_DB_PATH)

    try:
        # Get HYDAT version date
        version, version_date_str = query_hydat_version(conn)
        if version_date_str is None:
            return None

        # Parse version date
        try:
            version_date = datetime.strptime(version_date_str.split()[0], "%Y-%m-%d")
        except (ValueError, IndexError):
            version_date = datetime.strptime(version_date_str, "%Y-%m-%d")

        # Query active stations with coordinates
        query = """
        SELECT STATION_NUMBER, STATION_NAME, LONGITUDE, LATITUDE, HYD_STATUS
        FROM STATIONS
        WHERE HYD_STATUS = 'A'
          AND LONGITUDE IS NOT NULL
          AND LATITUDE IS NOT NULL
        """
        stations_df = pd.read_sql_query(query, conn)

        if stations_df.empty:
            return None

        # For each station, find most recent flow data
        backlog_data = []
        for _, row in stations_df.iterrows():
            station_id = row['STATION_NUMBER']

            # Query most recent flow data
            flow_query = """
            SELECT MAX(YEAR) as max_year, MAX(MONTH) as max_month
            FROM DLY_FLOWS
            WHERE STATION_NUMBER = ?
            """
            flow_df = pd.read_sql_query(flow_query, conn, params=(station_id,))

            if flow_df.empty or pd.isna(flow_df['max_year'].iloc[0]):
                continue

            max_year = int(flow_df['max_year'].iloc[0])
            max_month = int(flow_df['max_month'].iloc[0])

            # Get last day of month
            day_query = """
            SELECT NO_DAYS
            FROM DLY_FLOWS
            WHERE STATION_NUMBER = ? AND YEAR = ? AND MONTH = ?
            """
            day_df = pd.read_sql_query(day_query, conn, params=(station_id, max_year, max_month))

            if not day_df.empty:
                last_day = int(day_df['NO_DAYS'].iloc[0]) if not pd.isna(day_df['NO_DAYS'].iloc[0]) else 1
                try:
                    most_recent_date = datetime(max_year, max_month, last_day)
                except ValueError:
                    most_recent_date = datetime(max_year, max_month, 1)

                backlog_days = (version_date - most_recent_date).days

                backlog_data.append({
                    'station_id': station_id,
                    'station_name': row['STATION_NAME'],
                    'latitude': row['LATITUDE'],
                    'longitude': row['LONGITUDE'],
                    'last_record_date': most_recent_date.strftime('%Y-%m-%d'),
                    'backlog_days': backlog_days
                })

        conn.close()

        if not backlog_data:
            return None

        # Create DataFrame
        df = pd.DataFrame(backlog_data)
        df = df.dropna(subset=['latitude', 'longitude'])

        if df.empty:
            return None

        # Add derived columns
        gdf = gpd.GeoDataFrame(
            df,
            geometry=gpd.points_from_xy(df.longitude, df.latitude),
            crs="EPSG:4326"
        )
        gdf = gdf.to_crs(epsg=3857)

        df['x'] = gdf.geometry.x
        df['y'] = gdf.geometry.y
        df['category'] = df['backlog_days'].apply(categorize_backlog_duration)
        df['color'] = df['category'].map(backlog_colors)
        df['url'] = df['station_id'].apply(_station_page_url)
        df['map_url'] = df['url'].apply(lambda v: v if v else '#')
        df['backlog_display'] = df['backlog_days'].apply(
            lambda d: f"{d} days ({d/365.25:.1f} years)" if d >= 365 else f"{d} days"
        )
        df['backlog_months'] = df['backlog_days'] / 30.44

        return df

    except Exception as e:
        conn.close()
        return None


def plot_backlog_map(df, shared_source=None):
    """Plot interactive map of station backlog.

    Args:
        df: DataFrame with backlog data (should be pre-sorted for CDF alignment)
        shared_source: Optional shared ColumnDataSource for linked plots

    Returns:
        Bokeh figure
    """
    if shared_source is None:
        shared_source = ColumnDataSource(df)

    # Create figure
    p = figure(
        title="",
        x_axis_type="mercator",
        y_axis_type="mercator",
        width=800,
        height=600,
        tools="pan,wheel_zoom,lasso_select,box_select,reset,save",
        toolbar_location="above",
        match_aspect=True
    )
    p.add_tile("CartoDB Positron", retina=True)

    # Plot each category separately for legend toggle using CDSView
    all_renderers = []
    for cat in ['<1 year', '1-2 years', '2-5 years', '5-10 years', '10+ years']:
        cat_df = df[df['category'] == cat]
        if not cat_df.empty:
            # Create a view that filters the shared source by category
            view = CDSView(filter=GroupFilter(column_name='category', group=cat))
            circles = p.scatter(
                'x', 'y',
                source=shared_source,
                view=view,
                size=12,
                color=backlog_colors[cat],
                line_color='black',
                line_width=1,
                alpha=0.8,
                selection_color=backlog_colors[cat],
                nonselection_alpha=0.2,
                legend_label=cat
            )
            all_renderers.append(circles)

    # Configure legend for toggling
    p.legend.location = "top_left"
    p.legend.title = "Data Backlog"
    p.legend.title_text_font_style = "bold"
    p.legend.click_policy = "hide"

    # Add hover tool for all renderers
    hover = HoverTool(
        renderers=all_renderers,
        tooltips=[
            ("Station", "@station_id"),
            ("Name", "@station_name"),
            ("Last Record", "@last_record_date"),
            ("Backlog", "@backlog_display"),
            ("Category", "@category"),
            ("", "Click to view station page")
        ]
    )
    p.add_tools(hover)

    # Add tap tool for clickable links
    tap = TapTool()
    p.add_tools(tap)
    p.select(type=TapTool).callback = OpenURL(url='@map_url')

    return p


def plot_backlog_cdf(df=None, shared_source=None):
    """Generate CDF plot for data reporting backlog in month-equivalents.

    Args:
        df: DataFrame with backlog data (should already be sorted with cdf_y column)
        shared_source: Optional shared ColumnDataSource for linked plots

    Returns:
        Bokeh figure with CDF line and percentile annotations
    """
    # If no data provided, collect it
    if df is None:
        df = collect_backlog_data()
        if df is None:
            from bokeh.models import Div
            return Div(text="<p>No backlog data available.</p>")

    # Assume data is already sorted with cdf_y (done in plot_network_backlog)
    if shared_source is None:
        shared_source = ColumnDataSource(df)

    # Create figure
    p = figure(
        title="Cumulative Distribution of Data Reporting Backlog",
        x_axis_label="Backlog (months)",
        y_axis_label="Cumulative Probability",
        width=800,
        height=250,
        tools="pan,box_select,lasso_select,wheel_zoom,reset,save",
        toolbar_location="right",
        x_axis_type='log'
    )

    # Plot CDF
    cdf_circles = p.scatter(
        'backlog_months', 'cdf_y',
        source=shared_source,
        size=6,
        color='color',
        alpha=0.7,
        selection_color='color',
        selection_alpha=1.0,
        nonselection_alpha=0.2
    )

    # Add connecting line
    p.line(
        'backlog_months', 'cdf_y',
        source=shared_source,
        line_width=2,
        color='grey',
        alpha=0.6
    )

    # Add percentile annotations
    percentiles = [25, 50, 75, 90]
    for pct in percentiles:
        idx = int(len(df) * pct / 100)
        if idx < len(df):
            value = df.iloc[idx]['backlog_months']
            p.line([value, value], [0, pct/100], line_dash='dashed',
                   line_width=1, color='gray', alpha=0.5)

    # Styling
    p.y_range.start = -0.01
    p.y_range.end = 1.01

    # Add hover
    hover_cdf = HoverTool(
        renderers=[cdf_circles],
        tooltips=[
            ("Station", "@station_id"),
            ("Backlog", "@backlog_display"),
            ("Category", "@category"),
        ]
    )
    p.add_tools(hover_cdf)

    return p


def plot_network_backlog():
    """Generate linked map and CDF showing backlog of daily flow data reporting.

    Creates an interactive visualization where selecting points in the CDF highlights
    corresponding stations on the map via a shared data source.

    Returns:
        Bokeh column layout containing map and CDF with linked selections
    """
    # Collect backlog data
    df = collect_backlog_data()

    if df is None or df.empty:
        from bokeh.models import Div
        return Div(text="<p>No backlog data available.</p>")

    # Sort data once for CDF and add cdf_y column
    df_sorted = df.sort_values('backlog_months').reset_index(drop=True)
    df_sorted['cdf_y'] = (df_sorted.index + 1) / len(df_sorted)

    # Create shared data source with sorted data
    shared_source = ColumnDataSource(df_sorted)

